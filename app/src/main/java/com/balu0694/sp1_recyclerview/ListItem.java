package com.balu0694.sp1_recyclerview;

public class ListItem {

    public String head;
    public String desc;

    public ListItem(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
